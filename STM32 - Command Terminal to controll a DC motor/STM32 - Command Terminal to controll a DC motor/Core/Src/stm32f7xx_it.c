void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if(htim == &htim4)
		{
			HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_14);
		}
	if(htim == &htim3)
		{
			if((ADC_counter >= Samples) && (Samples!=0))
				{
					ADC_counter = 0;
					HAL_TIM_Base_Stop_IT(&htim3);
					can_print = 1;		//Ã‰ uma flag para dizer que acabou a amostragem e pode dar print no main
				}
			else
			{
				HAL_ADC_Start_IT(&hadc1);
			}
		}
}

void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef *hadc)
{
	if (hadc == &hadc1)
		{

			ADC_VALUE = HAL_ADC_GetValue(&hadc1);
			Volts = ((double)ADC_VALUE / ((double)4095)) * 3.3;
			ADC_Buffer [ADC_counter] = Volts;
			ADC_counter++;

			HAL_ADC_Stop_IT(&hadc1);
		}
}
